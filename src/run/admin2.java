    private void jButton27ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton27ActionPerformed
        // TODO add your handling code here:
        if(tanggalbeli1.getDate()== null || tanggalbeli2.getDate()== null){
            JOptionPane.showMessageDialog(rootPane, "Masukkan Tanggal Dahulu !");
        }else{
            String tampilan = "yyyy-MM-dd";
            SimpleDateFormat fm = new SimpleDateFormat(tampilan);
            String tanggal1 = String.valueOf(fm.format(tanggalbeli1.getDate()));
            String tanggal2 = String.valueOf(fm.format(tanggalbeli2.getDate()));

            try {

                HashMap parameter = new HashMap();

                parameter.put("tanggal3", tanggal1);
                parameter.put("tanggal4", tanggal2);

                JasperPrint jp = JasperFillManager.fillReport(getClass().
                    getResourceAsStream("laporbeli.jasper"),
                    parameter, koneksi.koneksi());
                JasperViewer.viewReport(jp, false);
            } catch(Exception e) {
                JOptionPane.showMessageDialog(rootPane, e);
            }
        }
    }//GEN-LAST:event_jButton27ActionPerformed

